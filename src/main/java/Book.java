class Book{
  String title;
  String author;
  int year;
  int pages;

  public Book(String title, String author, int year, int pages){
    this.title = title;
    this.author = author;
    this.year = year;
    this.pages = pages;
  }
  public Book(String title, String author){
    this.title = title;
    this.author = author;
    
  }

}